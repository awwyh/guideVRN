$(document).ready(function() {

   
    $("a.scroll-to").click(function() {
        $("html, body").animate({
            scrollTop: $($(this).attr("href")).offset().top + "px"
        }, {
            duration: 500,
            easing: "swing"
        });
        document.getElementById('top_menu_check').checked = false;
        return false;
    });
});